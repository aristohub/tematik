      <p>Few jewelry brands have such an illustrious history as the House of Harry Winston. 
      Founded in New York City in 1932, by Mr. Harry Winston – an innate gemologist, an intuitive business man, and a dedicated philanthropist – the brand continues to set the standard for the ultimate in fine jewelry and high-end watchmaking. 
      Known throughout his life as the "King of Diamonds", and the "Jeweler to the Stars", Mr. Winston's innovative design philosophy – in which the individual gemstones, rather than the metal settings, would dictate each design – helped to revolutionize fine jewelry designs, and remains at the cornerstone of a timeless aesthetic that continues to inspire all Harry Winston creations. 
      From the acquisition of some of the world's most famous gemstones, including the Jonker, Hope, and Winston Legacy Diamonds, to adorning generations of famous faces, from Hollywood legends to international Heads of State, for over eight decades, the Harry Winston name has been synonymous with the best that there is. 
      Today, the House of Harry Winston continues its tradition of creativity, rarity, and quality without compromise in its retail salons around the world, including: New York, London, Paris, Geneva, Tokyo, Hong Kong, and Shanghai.
